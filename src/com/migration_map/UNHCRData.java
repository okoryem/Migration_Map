package com.migration_map;

public class UNHCRData {
    private int year;
    private String coo;
    private String coa;
    private int refugees;
    private int asylumSeekers;

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public String getCoo() {
        return coo;
    }

    public void setCoo(String coo) {
        this.coo = coo;
    }

    public String getCoa() {
        return coa;
    }

    public void setCoa(String coa) {
        this.coa = coa;
    }

    public int getRefugees() {
        return refugees;
    }

    public void setRefugees(int refugees) {
        this.refugees = refugees;
    }

    public int getAsylumSeekers() {
        return asylumSeekers;
    }

    public void setAsylumSeekers(int asylumSeekers) {
        this.asylumSeekers = asylumSeekers;
    }
}
